import assert from 'power-assert';
import { searchMatrix } from '../searchMatrix2';

describe('src/searchMatrix2', () => {
  describe('searchMatrix', () => {
    it('[[1,4,7,11,15],[2,5,8,12,19],[3,6,9,16,22],[10,13,14,17,24],[18,21,23,26,30]], 5', () =>
      assert.ok(
        searchMatrix(
          [
            [1, 4, 7, 11, 15],
            [2, 5, 8, 12, 19],
            [3, 6, 9, 16, 22],
            [10, 13, 14, 17, 24],
            [18, 21, 23, 26, 30]
          ],
          5
        )
      ));
    it('[[1,4,7,11,15],[2,5,8,12,19],[3,6,9,16,22],[10,13,14,17,24],[18,21,23,26,30]], 20', () =>
      assert.ok(
        !searchMatrix(
          [
            [1, 4, 7, 11, 15],
            [2, 5, 8, 12, 19],
            [3, 6, 9, 16, 22],
            [10, 13, 14, 17, 24],
            [18, 21, 23, 26, 30]
          ],
          20
        )
      ));
    it('[[3,3,8,13,13,18],[4,5,11,13,18,20],[9,9,14,15,23,23],[13,18,22,22,25,27],[18,22,23,28,30,33],[21,25,28,30,35,35],[24,25,33,36,37,40]],21', () =>
      assert.ok(
        searchMatrix(
          [
            [3, 3, 8, 13, 13, 18],
            [4, 5, 11, 13, 18, 20],
            [9, 9, 14, 15, 23, 23],
            [13, 18, 22, 22, 25, 27],
            [18, 22, 23, 28, 30, 33],
            [21, 25, 28, 30, 35, 35],
            [24, 25, 33, 36, 37, 40]
          ],
          21
        )
      ));
  });
});
