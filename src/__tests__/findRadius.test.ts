import assert from 'power-assert';
import { findRadius } from '../findRadius';

describe('src/findRadius', () => {
  describe('findRadius', () => {
    it('[1, 2, 3], [2]', () => assert.strictEqual(findRadius([1, 2, 3], [2]), 1));
    it('[1,2,3,4], [1,4]', () => assert.strictEqual(findRadius([1, 2, 3, 4], [1, 4]), 1));
    it('[1,5], [2]', () => assert.strictEqual(findRadius([1, 5], [2]), 3));
    it('[282475249,622650073,984943658,144108930,470211272,101027544,457850878,458777923],[823564440,115438165,784484492,74243042,114807987,137522503,441282327,16531729,823378840,143542612]', () =>
      assert.strictEqual(
        findRadius(
          [282475249, 622650073, 984943658, 144108930, 470211272, 101027544, 457850878, 458777923],
          [
            823564440,
            115438165,
            784484492,
            74243042,
            114807987,
            137522503,
            441282327,
            16531729,
            823378840,
            143542612
          ]
        ),
        161834419
      ));
  });
});
